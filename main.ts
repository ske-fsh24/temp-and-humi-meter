input.onButtonPressed(Button.A, function on_button_pressed_a() {
    I2C_LCD1602.LcdInit(0)
    I2C_LCD1602.BacklightOn()
    I2C_LCD1602.ShowString("welcome", 0, 0)
    I2C_LCD1602.ShowString("access grounded", 0, 1)
    basic.pause(5000)
    I2C_LCD1602.clear()
})
